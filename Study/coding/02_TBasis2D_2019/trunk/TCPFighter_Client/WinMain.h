extern HWND			g_hWnd;
extern HINSTANCE	g_hInstance;
extern BOOL			g_bActiveApp;

/////////////////////////////////////////////////////////////////////

/////////////////////////////////////////////////////////////////////
// 윈도우 초기화. 
//
